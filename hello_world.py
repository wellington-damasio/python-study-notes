# What is python?
# 1) High-level
# Python is a high-level programming language.

# 2) General purpose
# Python is a general-purpose language. It means that you can use Python in various domains, including:
    # - Web applications
    # - Big data applications
    # - Testing
    # - Automation
    # - Data science, machine learning, AI
    # - Desktop software
    # - Mobile apps

# 3) Interpreted
# Python is a interpreted language. To develop a Python program you write Python code into a file
# called source code.
#
# To execute source code, you need to convert it to machine language that the computer can understand.
# And Python interpreter turns the source code, line by line, once at a time, into the machine code
# when the Python program executes

