# To represent true and false, Python provides you with the boolean data type. The boolean value has
#a technical name as bool

# The boolean data type has two values: True and False
is_active = True
is_admin = False

print(is_active)
print(is_admin)

# When you compare two numbers, Python returns the result as a boolean value
print(20 < 10) # False
print(10 > 5) # True

# ---------------------------------------
#          The bool() function
# ---------------------------------------
# To find out if a value is True or False, you use the bool() function
print(bool('Hi')) # True
print(bool('')) # False
print(bool(1)) # True
print(bool(0)) # False

